import Foundation
import UIKit

extension UIFont {
    
    class func avenirHeavyFontOfSize(_ size: CGFloat) -> UIFont! {
        
        return UIFont(name: "Avenir-Heavy", size: size)
    }

    class func avenirMediumFontOfSize(_ size: CGFloat) -> UIFont! {
        
        return UIFont(name: "Avenir-Medium", size: size)
    }

    
}

